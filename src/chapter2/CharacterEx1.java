package chapter2;

public class CharacterEx1 {
    static void main() {
        char ch1 = 'A'; //문자타입은 한따옴표
        System.out.println(ch1);
        System.out.println((int)ch1); //문자A의 정수값 65

        char ch2 = 66; //문자타입은 한따옴표
        System.out.println(ch2);

        char ch3 = 67; //문자타입은 한따옴표
        System.out.println(ch3);
        System.out.println((int)ch3);
    }
}
